  private ConnectionFactory connectionFactoryElement(XNode context) throws Exception {
    if (context != null) {
      Properties properties = context.getChildrenAsProperties();
      ConnectionFactoryOptions.Builder connectionFactoryOptionsBuilder = ConnectionFactoryOptions.builder();
      Properties poolProperties = new Properties();
      for (Object key : properties.keySet()) {
        String propertyName = (String) key;
        if(propertyName.equals("url")){
          connectionFactoryOptionsBuilder = ConnectionFactoryOptions.parse(properties.getProperty(propertyName)).mutate();
          break;
        }
      }
      for (Object key : properties.keySet()) {
        String propertyName = (String) key;
        String value = properties.getProperty(propertyName);
        String POOL_PREFIX = "pool.";
        if(propertyName.startsWith(POOL_PREFIX)){
          poolProperties.put(propertyName.substring(POOL_PREFIX.length()), value);
        } else {
          connectionFactoryOptionsBuilder.option(Option.valueOf(propertyName),value);
        }
      }
      ConnectionFactoryOptions connectionFactoryOptions = connectionFactoryOptionsBuilder.build();
      ConnectionFactory factory = ConnectionFactories.get(connectionFactoryOptions);

      String type = context.getStringAttribute("type");
      if("R2DBC_POOLED".equalsIgnoreCase(type)){
        ConnectionPoolConfiguration poolConfiguration = ConnectionPoolConfiguration.builder(factory).build();
        MetaObject metaDataSource = SystemMetaObject.forObject(poolConfiguration);
        for (Object key : poolProperties.keySet()) {
          String propertyName = (String) key;
          String value = properties.getProperty(propertyName);
          if (metaDataSource.hasSetter(propertyName)) {
            Object convertedValue = convertValue(metaDataSource, propertyName, value);
            metaDataSource.setValue(propertyName, convertedValue);
          }else {
            throw new DataSourceException("Unknown ConnectionPoolConfiguration property: " + propertyName);
          }
          factory = (ConnectionFactory) resolveClass(type).getDeclaredConstructor(ConnectionPoolConfiguration.class).newInstance(poolConfiguration);
        }
      }
      return factory;
    }
    throw new BuilderException("Environment declaration requires a ConnectionFactory.");
  }

  private Object convertValue(MetaObject metaDataSource, String propertyName, String value) {
    Object convertedValue = value;
    Class<?> targetType = metaDataSource.getSetterType(propertyName);
    if (targetType == Integer.class || targetType == int.class) {
      convertedValue = Integer.valueOf(value);
    } else if (targetType == Long.class || targetType == long.class) {
      convertedValue = Long.valueOf(value);
    } else if (targetType == Boolean.class || targetType == boolean.class) {
      convertedValue = Boolean.valueOf(value);
    }else if(targetType == Duration.class){
      convertedValue = Duration.ofMinutes(Long.parseLong(value));
    }
    return convertedValue;
  }


  private void typeHandlerElement(XNode parent) {
    if (parent != null) {
      for (XNode child : parent.getChildren()) {
        if ("package".equals(child.getName())) {
          String typeHandlerPackage = child.getStringAttribute("name");
          typeHandlerRegistry.register(typeHandlerPackage);
        } else {
          String javaTypeName = child.getStringAttribute("javaType");
          String jdbcTypeName = child.getStringAttribute("jdbcType");
          String handlerTypeName = child.getStringAttribute("handler");
          Class<?> javaTypeClass = resolveClass(javaTypeName);
          JdbcType jdbcType = resolveJdbcType(jdbcTypeName);
          Class<?> typeHandlerClass = resolveClass(handlerTypeName);
          if (javaTypeClass != null) {
            if (jdbcType == null) {
              typeHandlerRegistry.register(javaTypeClass, typeHandlerClass);
            } else {
              typeHandlerRegistry.register(javaTypeClass, jdbcType, typeHandlerClass);
            }
          } else {
            typeHandlerRegistry.register(typeHandlerClass);
          }
        }
      }
    }
  }

  private void mapperElement(XNode parent) throws Exception {
    if (parent != null) {
      for (XNode child : parent.getChildren()) {
        if ("package".equals(child.getName())) {
          String mapperPackage = child.getStringAttribute("name");
          configuration.addMappers(mapperPackage);
        } else {
          String resource = child.getStringAttribute("resource");
          String url = child.getStringAttribute("url");
          String mapperClass = child.getStringAttribute("class");
          if (resource != null && url == null && mapperClass == null) {
            ErrorContext.instance().resource(resource);
            try(InputStream inputStream = Resources.getResourceAsStream(resource)) {
              XMLMapperBuilder mapperParser = new XMLMapperBuilder(inputStream, configuration, resource, configuration.getSqlFragments());
              mapperParser.parse();
            }
          } else if (resource == null && url != null && mapperClass == null) {
            ErrorContext.instance().resource(url);
            try(InputStream inputStream = Resources.getUrlAsStream(url)){
              XMLMapperBuilder mapperParser = new XMLMapperBuilder(inputStream, configuration, url, configuration.getSqlFragments());
              mapperParser.parse();
            }
          } else if (resource == null && url == null && mapperClass != null) {
            Class<?> mapperInterface = Resources.classForName(mapperClass);
            configuration.addMapper(mapperInterface);
          } else {
            throw new BuilderException("A mapper element may only specify a url, resource or class, but not more than one.");
          }
        }
      }
    }
  }

  private boolean isSpecifiedEnvironment(String id) {
    if (environment == null) {
      throw new BuilderException("No environment specified.");
    }
    if (id == null) {
      throw new BuilderException("Environment requires an id attribute.");
    }
    return environment.equals(id);
  }

